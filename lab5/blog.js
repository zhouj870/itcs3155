/**
 * This function creates a DOM elment with information from the blog object, and adds the element into the DOM.
 * @param {*} blog - a blog element
 */
function addEntry(blog) {
  let container  = document.querySelector(".posts");

  let article = document.createElement("article");
  article.classList.add("post");

  let title = document.createElement("h3");
  title.innerText = blog.title;
  title.classList.add("blog-header");

  let date = document.createElement("p");
  date.classList.add("date");
  date.innerText = `${blog.date.getMonth() + 1}/${blog.date.getDate()}/${blog.date.getFullYear()}`;

  let content = document.createElement("p");
  content.innerText = blog.content;

  let x = document.createElement("button");
  x.innerText = ("[x]");
  x.classList.add("delete-btn");

  article.appendChild(title);
  article.appendChild(date);
  article.appendChild(content);
  title.appendChild(x);

  container.appendChild(article);

  
  

}

